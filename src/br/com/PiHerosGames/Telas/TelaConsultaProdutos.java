    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        
        modelProdutos.setNome_produto(this.txt_nomeProd.getText());
        modelProdutos.setMarca_produto(this.txt_MarcaProd.getText());
        modelProdutos.setPreco_produtos(Double.parseDouble(this.txt_precoProd.getText()));
        modelProdutos.setCaracteristica_produtos(this.txt_caractProd.getText());
        modelProdutos.setEstoque_produtos(Integer.parseInt(this.txt_estoqueProd.getText()));
        if(controllerProdutos.salvarProdutosController(modelProdutos) > 0){
            JOptionPane.showMessageDialog(null, "Produto Cadastrado com sucesso!");
            LimparPainel();
            this.garregarProdutos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar !");
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int linha = tbl_dadosProd.getSelectedRow();
        int pIdtb_vendas = (int) tbl_dadosProd.getValueAt(linha, 0);
        int deletar = JOptionPane.showConfirmDialog(null, " Tem certeza que deseja excluir o produto ?", "Atenção", JOptionPane.YES_NO_OPTION);
        
        
        if(deletar == JOptionPane.YES_OPTION){
            if(controllerProdutos.excluirProdutosController(pIdtb_vendas)){
                JOptionPane.showMessageDialog(null, "Produto Deletado com sucesso!");
                LimparPainel();
                this.garregarProdutos();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao Deletar !");
            }
            
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

