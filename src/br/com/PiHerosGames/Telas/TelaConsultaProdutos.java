    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        this.habilitarDesabilitarCampos(true);
        int linhaSelect = tbl_dadosProd.getSelectedRow();
        int codigoProdSelect = (int) tbl_dadosProd.getValueAt(linhaSelect, 0);
        btn_Atualizar.setEnabled(true);
        try {
            modelProdutos = controllerProdutos.getProdutosController(codigoProdSelect);
            getValuesModelProduto(modelProdutos);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarActionPerformed
        int editar = JOptionPane.showConfirmDialog(null, " Tem certeza que deseja Editar o produto ?", "Atenção", JOptionPane.YES_NO_OPTION);
        modelProdutos.setIdtb_vendas(Integer.parseInt(this.txt_idProd.getText()));
        modelProdutos.setNome_produto(this.txt_nomeProd.getText());
        modelProdutos.setMarca_produto(this.txt_MarcaProd.getText());
        modelProdutos.setPreco_produtos(Double.parseDouble(this.txt_precoProd.getText()));
        modelProdutos.setCaracteristica_produtos(this.txt_caractProd.getText());
        modelProdutos.setEstoque_produtos(Integer.parseInt(this.txt_estoqueProd.getText()));
         if(editar == JOptionPane.YES_OPTION){
            if(controllerProdutos.atualizarProdutosController(modelProdutos)){
                JOptionPane.showMessageDialog(null, "Produto Atualizado com sucesso!");
                LimparPainel();
                this.habilitarDesabilitarCampos(false);
                this.garregarProdutos();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao Atualizar !");
            }
        }
    }//GEN-LAST:event_btn_AtualizarActionPerformed

