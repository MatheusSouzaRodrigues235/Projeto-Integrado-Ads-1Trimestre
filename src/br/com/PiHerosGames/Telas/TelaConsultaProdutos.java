    //Preenche a tabela de produtos
    private void garregarProdutos() {
        listaProdutos = controllerProdutos.getListaProdutosController();
        DefaultTableModel modelo = (DefaultTableModel) tbl_dadosProd.getModel();
        modelo.setNumRows(0);
        // Inserção de produtos na tabela
        int cont = listaProdutos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
               listaProdutos.get(i).getIdtb_vendas(),
               listaProdutos.get(i).getNome_produto(),
               listaProdutos.get(i).getMarca_produto(),
               listaProdutos.get(i).getPreco_produtos(),
               listaProdutos.get(i).getCaracteristica_produtos(),
               listaProdutos.get(i).getEstoque_produtos()
            });
        }
    }
    private void habilitarDesabilitarCampos(boolean condicao){
        txt_nomeProd.setEnabled(condicao);
        txt_MarcaProd.setEnabled(condicao);
        txt_precoProd.setEnabled(condicao);
        txt_caractProd.setEnabled(condicao);
        txt_estoqueProd.setEnabled(condicao);
    }
    
    public void getValuesModelProduto(ModelProdutos pModelProdutos) {
        this.txt_idProd.setText(Integer.toString(pModelProdutos.getIdtb_vendas()));
        this.txt_nomeProd.setText(pModelProdutos.getNome_produto());
        this.txt_MarcaProd.setText(pModelProdutos.getMarca_produto());
        this.txt_precoProd.setText(Double.toString(pModelProdutos.getPreco_produtos()));
        this.txt_caractProd.setText(pModelProdutos.getCaracteristica_produtos());
        this.txt_estoqueProd.setText( Integer.toString(pModelProdutos.getEstoque_produtos()));
    }
    
